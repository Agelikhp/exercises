public class ArrayPopulateUnsized {

    public static void main(String[] args) {
        //Initialize - populate array
        int [] ages = {19, 50, 23, 44, 30};

        //Print the array elements
        for (int i = 0; i < ages.length; i++) {
            System.out.print(ages[i] + " ");
        }
    }
}
